public class CreateAnArray {
    public static void main(String[] args) {

        // Creating an array
        int[] arr = new int[5];

        // Assigning values
        arr[0] = 10;
        arr[1] = 20;
        arr[2] = 30;
        arr[3] = 40;
        arr[4] = 50;

        // Printing array elements
        for (int i = 0; i < arr.length; i++) {
            System.out.println(arr[i]);
        }
    }
}

